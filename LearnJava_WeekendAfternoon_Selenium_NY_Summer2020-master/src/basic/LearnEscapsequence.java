package basic;

public class LearnEscapsequence {
	// Escape sequence:
	
	public static void main(String[] args) {
		System.out.println("\"James\"");
		System.out.println("William");
		System.out.println("\'Lem Lem\'");
		System.out.println("\\William\\");
		System.out.println("We are Learning "+"\n"+"Escape Sequence");
		System.out.println("We are Learning \nEscape Sequence");
		System.out.println("We are Learning \tEscape Sequence");
		
		System.out.println("We are Learning Escape Sequence1\b");
		
		
	}
	

}
